void SSD1306_Buffer_swap(void)
{
    if (buffer == buffer1)
    {
        buffer = buffer2; // Switch to buffer2
        memcpy(buffer2, buffer1, buffer_size); // Copy contents of buffer1 to buffer2
    }
    else
    {
        buffer = buffer1; // Switch back to buffer1
        memcpy(buffer1, buffer2, buffer_size); // Copy contents of buffer1 to buffer2
    }
}

